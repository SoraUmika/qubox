        if_freq = self.calculate_el_if_fq(el, freq)
        self.qm.set_intermediate_frequency(el, if_freq)
        self.elements[el]["IF"] = if_freq
        _logger.info(f"Set element {el} to frequency: {freq * 1e-6:.3f} MHz -> IF: {if_freq * 1e-6:.3f} MHz")


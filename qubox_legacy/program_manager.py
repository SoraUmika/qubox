def _patch_octave_output_modes_in_json(cfg: dict, mode: RFOutputMode) -> None:
    str_mode = _JSON_MODE_MAP[mode]
    for _, oct_data in (cfg.get("octaves") or {}).items():
        rf_outs = oct_data.get("RF_outputs") or {}
        for _, ch_data in rf_outs.items():
            if isinstance(ch_data, dict):
                ch_data["output_mode"] = str_mode

class ExecMode(str, Enum):
    HARDWARE = "hardware"
    SIMULATE = "simulate"

def _coerce_exec_mode(mode: ExecMode | str | None) -> ExecMode:
    if mode is None:
        return ExecMode.HARDWARE
    if isinstance(mode, ExecMode):
        return mode
    s = str(mode).strip().lower()
    if s in ("hardware", "hw"):
        return ExecMode.HARDWARE
    if s in ("simulate", "sim"):
        return ExecMode.SIMULATE
    raise ValueError(f"Unknown ExecMode: {mode!r}")



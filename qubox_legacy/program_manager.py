    # ───────────────────────── logging control ─────────────────────────
    @contextlib.contextmanager
    def quiet_external_logs(
        self,
        *,
        qm_level: int = logging.WARNING,
        extra_loggers: Optional[Iterable[str]] = None,
    ):
        """
        Temporarily suppress noisy INFO logs from external libs (QM compilation spam, etc.).
        """
        names = ["qm"]
        if extra_loggers:
            names.extend(list(extra_loggers))

        old = []
        for name in names:
            lg = logging.getLogger(name)
            old.append((lg, lg.level))
            lg.setLevel(qm_level)
        try:
            yield
        finally:
            for lg, lvl in old:
                lg.setLevel(lvl)

    # ───────────────────────── queue submit wrappers ─────────────────────────
    def queue_submit(self, qua_prog, *, to_start: bool = False, quiet: bool = True):
        """
        Submit program to QM queue and return QmPendingJob.

        IMPORTANT:
        - Remembers this program + a config snapshot so serialize_program() works after
        queue-based workflows (e.g. your RB batching).
        """
        self._require_qm()

        # Snapshot config at submit-time so debug serialization matches what was submitted
        cfg_snapshot = self.qm_config or self.build_qm_config()

        # Remember program even if user never calls run_program()
        if hasattr(self, "_remember_program"):
            self._remember_program(
                qua_prog,
                cfg=cfg_snapshot,
                meta={
                    "last_mode": "hardware",
                    "queued": True,
                    "queue_submit": True,
                    "queue_to_start": bool(to_start),
                },
            )
        else:
            self.current_program = qua_prog

        cm = self.quiet_external_logs() if quiet else contextlib.nullcontext()
        with cm:
            with self._lock:
                try:
                    pending = self.qm.queue.add_to_start(qua_prog) if to_start else self.qm.queue.add(qua_prog)
                except StreamTerminatedError as e:
                    raise QMConnectionError("Connection lost while adding job to queue.") from e
                except Exception as e:
                    raise QMJobError(f"Failed to add job to queue: {e}") from e

        # Update remembered metadata with pending job info (best effort)
        if hasattr(self, "_remember_program"):
            self._remember_program(
                qua_prog,
                cfg=cfg_snapshot,
                meta={
                    "job_id": getattr(pending, "job_id", None),
                    "time_added": getattr(pending, "time_added", None),
                    "user_added": getattr(pending, "user_added", None),
                },
            )

        return pending

    def queue_submit_many(self, programs, *, to_start: bool = False, quiet: bool = True):
        """
        Submit many programs. The last submitted becomes current_program (serializable).
        """
        pendings = []
        for p in programs:
            pendings.append(self.queue_submit(p, to_start=to_start, quiet=quiet))
        return pendings

    # ───────────────────────── progress helpers ─────────────────────────
    @staticmethod
    def _progress_value(x) -> int:
        if x is None:
            return 0
        if isinstance(x, (int, np.integer)):
            return int(x)
        if isinstance(x, (float, np.floating)):
            return int(x)
        if isinstance(x, (list, tuple)) and len(x) > 0:
            return int(x[-1])
        if isinstance(x, np.ndarray):
            if x.size == 0:
                return 0
            return int(x.reshape(-1)[-1])
        try:
            return int(x)
        except Exception:
            return 0

    def _collect_output_from_job(self, job, *, print_report: bool):
        out = Output()
        if print_report:
            with contextlib.suppress(Exception):
                report = job.execution_report()
                if report:
                    _logger.info("Execution report:\n%s", report)
        for name, handle in list(job.result_handles.items()):
            try:
                out[name] = handle.fetch_all()
            except Exception as e:
                _logger.error(f"Fetch failed for handle '{name}': {e}")
        return out

    # ───────────────────────── run many queued jobs with ONE global bar ─────────────────────────
    def queue_run_many(
        self,
        pending_jobs: list,
        *,
        n_totals: int | list[int],
        processors: list | None = None,
        progress_handle: str = "iteration",
        show_total_progress: bool = True,
        print_report: bool = False,
        auto_job_halt: bool = True,
        quiet: bool = True,
        desc: str = "Running queued...",
        sleep_s: float = 0.05,
        **proc_kwargs,
    ) -> list["RunResult"]:
        """
        Run queued jobs sequentially, showing ONE global tqdm progress bar.

        Note: "most recent program for serialize_program()" is handled at submit-time
        in queue_submit(), since we don't reliably recover the QUA object from a pending/job.
        """
        self._require_qm()

        if isinstance(n_totals, int):
            n_list = [int(n_totals)] * len(pending_jobs)
        else:
            n_list = [int(x) for x in n_totals]
            _require(len(n_list) == len(pending_jobs), "n_totals length must match pending_jobs length.", QMJobError)

        total_iters = int(sum(n_list))
        procs = processors or self.processors

        show_bar = bool(show_total_progress) and _logger.isEnabledFor(logging.INFO)
        cm = self.quiet_external_logs() if quiet else contextlib.nullcontext()

        results: list[RunResult] = []
        offset = 0

        with cm, tqdm(total=total_iters, desc=desc, disable=not show_bar, leave=True) as bar:
            for pending, n_total in zip(pending_jobs, n_list):
                try:
                    job = pending.wait_for_execution()
                except StreamTerminatedError as e:
                    raise QMConnectionError("Connection lost while waiting for queued job execution.") from e
                except Exception as e:
                    raise QMJobError(f"Queued job failed to start: {e}") from e

                handles = job.result_handles
                last = 0

                if progress_handle in handles.keys():
                    while handles.is_processing():
                        time.sleep(float(sleep_s))
                        with contextlib.suppress(Exception):
                            h = handles.get(progress_handle)
                            if h is not None:
                                cur = self._progress_value(h.fetch_all())
                                cur = max(0, min(int(cur), int(n_total)))
                                if cur != last:
                                    last = cur
                                    bar.n = offset + last
                                    bar.refresh()

                    with contextlib.suppress(Exception):
                        h = handles.get(progress_handle)
                        if h is not None:
                            cur = self._progress_value(h.fetch_all())
                            cur = max(0, min(int(cur), int(n_total)))
                            last = cur
                            bar.n = offset + last
                            bar.refresh()
                else:
                    while handles.is_processing():
                        time.sleep(float(sleep_s))

                try:
                    out = self._collect_output_from_job(job, print_report=print_report)
                finally:
                    if auto_job_halt:
                        with contextlib.suppress(Exception):
                            job.halt()

                if procs:
                    for proc in procs:
                        try:
                            out = proc(out, **proc_kwargs)
                        except Exception as e:
                            pname = getattr(proc, "__name__", repr(proc))
                            _logger.error(f"Processor {pname} failed: {e}")

                bar.n = offset + int(n_total)
                bar.refresh()

                results.append(
                    RunResult(
                        mode=ExecMode.HARDWARE,
                        output=out,
                        sim_samples=None,
                        metadata={"n_total": int(n_total), "queued": True},
                    )
                )
                offset += int(n_total)

            bar.n = total_iters
            bar.refresh()

        return results

    # ───────────────────────── submit-many with progress bar ─────────────────────────
    def queue_submit_many_with_progress(
        self,
        programs,
        *,
        to_start: bool = False,
        quiet: bool = True,
        show_submit_progress: bool = True,
        desc: str = "Submitting to queue...",
    ) -> list:
        """
        Submit many programs to the queue with a progress bar (counts jobs submitted).

        The last submitted program becomes current_program (serializable).
        """
        self._require_qm()
        show_bar = bool(show_submit_progress) and _logger.isEnabledFor(logging.INFO)
        cm = self.quiet_external_logs() if quiet else contextlib.nullcontext()

        pendings = []
        with cm, tqdm(total=len(programs), desc=desc, disable=not show_bar, leave=True) as bar:
            for prog in programs:
                # queue_submit already remembers prog + cfg snapshot
                pending = self.queue_submit(prog, to_start=to_start, quiet=False)  # quiet handled by outer `cm`
                pendings.append(pending)
                bar.update(1)

        return pendings


    def hw_set_controller_io_offsets(
        self,
        *,
        controller: str,
        analog_outputs: dict[int, float] | None = None,
        analog_inputs: dict[int, float] | None = None,
        autosave: bool = True,
        autoburn: bool = True,
        allow_while_running: bool = False,
        create_if_missing: bool = True,
    ) -> None:
        """
        Bulk setter for controller analog IO offsets.

        Example
        -------
        hw_set_controller_io_offsets(
            controller="con1",
            analog_outputs={1: 0.0, 2: 0.0, 5: -0.002},
            analog_inputs={1: 0.00947713, 2: 0.00962465},
        )
        """
        ao_map = dict(analog_outputs or {})
        ai_map = dict(analog_inputs or {})

        def _patch(hw: dict, extras: dict) -> None:
            ctrls = hw.setdefault("controllers", {})
            _require(controller in ctrls, f"Unknown controller '{controller}' in hardware.", QMConfigError)
            cdict = ctrls[controller]
            _require(isinstance(cdict, dict), f"controllers['{controller}'] is not a dict", QMConfigError)

            if ao_map:
                aouts = cdict.setdefault("analog_outputs", {})
                _require(isinstance(aouts, dict), f"controllers['{controller}'].analog_outputs is not a dict", QMConfigError)
                for p, off in ao_map.items():
                    k = _key_like(aouts, int(p))
                    if k not in aouts:
                        if not create_if_missing:
                            raise QMConfigError(
                                f"controllers['{controller}'].analog_outputs has no port {p}."
                            )
                        aouts[k] = {}
                    ch = aouts[k]
                    _require(isinstance(ch, dict), f"analog_outputs[{p}] is not a dict", QMConfigError)
                    ch["offset"] = float(off)

            if ai_map:
                ains = cdict.setdefault("analog_inputs", {})
                _require(isinstance(ains, dict), f"controllers['{controller}'].analog_inputs is not a dict", QMConfigError)
                for p, off in ai_map.items():
                    k = _key_like(ains, int(p))
                    if k not in ains:
                        if not create_if_missing:
                            raise QMConfigError(
                                f"controllers['{controller}'].analog_inputs has no port {p}."
                            )
                        ains[k] = {}
                    ch = ains[k]
                    _require(isinstance(ch, dict), f"analog_inputs[{p}] is not a dict", QMConfigError)
                    ch["offset"] = float(off)

        self.patch_hardware(_patch, autosave=autosave, autoburn=autoburn, allow_while_running=allow_while_running)



# ───────────────────────────────── Main Manager ───────────────────────────────
class QuaProgramManager:
    """
    Refactor summary:
      - hardware_base + hardware_extras are the persisted truth (hardware.json)
      - pulse_overlay is generated (pulses/waveforms/weights/etc)
      - runtime_overrides are ephemeral patches you may or may not persist
      - qm_config is an artifact built by build_qm_config()
      - apply_changes() rebuilds + reopens QM (the “burn” operation)
    """

    # ───────────────────────────── init ─────────────────────────────
    def __init__(
        self,
        qop_ip: str,
        cluster_name: str,
        oct_cal_path: str,
        *,
        hardware_path: str | Path | None = None,
        config_dict: dict | None = None,
        default_output_mode: ModeLike = "on",
        override_octave_json_mode: Optional[ModeLike] = None,
        hardware_extras_keys: Optional[set[str]] = None,
    ):
        self._qmm = QuantumMachinesManager(
            host=qop_ip,
            cluster_name=cluster_name,
            octave_calibration_db_path=oct_cal_path,
        )
        self.qm: QuantumMachine | None = None
        self.job: Optional[RunningQmJob] = None
        self._lock = threading.RLock()

        # Persisted IO
        self.hardware_path: Optional[Path] = Path(hardware_path) if hardware_path else None
        self._hardware_extras_keys = set(_HARDWARE_EXTRAS_KEYS_DEFAULT if hardware_extras_keys is None else hardware_extras_keys)

        # Layered state
        self.hardware_base: dict | None = None
        self.hardware_extras: dict = {}
        self.pulse_overlay: dict = {}
        self.element_ops_overlay: dict = {}
        self.runtime_overrides: dict = {}
        self.qm_config: dict | None = None

        self._dirty: dict[str, bool] = {"hardware": False, "pulses": False, "runtime": False}

        # book-keeping
        self.config_exists = False
        self.elements: Dict[str, Dict] = {}
        self.processors: List[Callable] = [proc_default]
        self.spa_pump_sc = None
        self._device_manager = None                  # optional DeviceManager for external LO sources
        self.current_program = None                  # backward-compat alias: "most recently submitted/run/simulated"
        self._last_program_cfg: dict | None = None   # config snapshot used for last program
        self._last_program_meta: dict = {}           # optional metadata (mode, queued, timestamps, job_id...)
        self._last_program_ts: float | None = None

        # output-mode preferences
        self._default_output_mode: RFOutputMode | None = self._coerce_rf_mode(default_output_mode)
        self._override_octave_json_mode: RFOutputMode | None = self._coerce_rf_mode(override_octave_json_mode)

        # Load hardware.json (if provided)
        self.load_hardware(self.hardware_path)

        # Optional: allow constructing from a provided full QM config
        if config_dict is not None:
            cfg = _numeric_keys_to_ints(_filter_qm_schema(config_dict))
            self.hardware_base = _filter_hardware_schema(cfg)
            self.hardware_extras = _select_keys(config_dict, self._hardware_extras_keys)
            self.pulse_overlay = {k: deepcopy(cfg.get(k, {})) for k in _PULSE_KEYS if k in cfg}
            self.runtime_overrides = {}
            self._dirty = {"hardware": False, "pulses": False, "runtime": False}

        if self.hardware_base is not None or config_dict is not None:
            self.apply_changes(save_hardware=False, allow_while_running=True)

        self.exec_mode: ExecMode = ExecMode.HARDWARE


    # ─────────────────────────── internal guards ──────────────────────
    def _remember_program(self, qua_prog, *, cfg: dict | None = None, meta: dict | None = None) -> None:
        """
        Record the most recent program and (optionally) a snapshot of the config used.
        This is what serialize_program() will use by default.
        """
        with self._lock:
            self.current_program = qua_prog
            self._last_program_ts = time.time()
            if cfg is not None:
                self._last_program_cfg = deepcopy(cfg)
            if meta:
                self._last_program_meta = {**self._last_program_meta, **deepcopy(meta)}


    def _require_qm(self):
        _require(self.qm is not None, "QM not initialized; call apply_changes() or init_qm().", QMError)

    def _check_el(self, el: str):
        _require(el in self.elements, f"Unknown element '{el}'", QMConfigError)

    @staticmethod
    def _coerce_rf_mode(mode: Optional[ModeLike]) -> Optional[RFOutputMode]:
        if mode is None or isinstance(mode, RFOutputMode):
            return mode
        return {
            "on": RFOutputMode.on,
            "off": RFOutputMode.off,
            "debug": RFOutputMode.debug,
            "trig_normal": RFOutputMode.trig_normal,
            "trig_inverse": RFOutputMode.trig_inverse,
        }.get(mode)

    # ────────────────────────── external controls ─────────────────────
    def set_spa_pump(self, spa_pump_sc):
        self.spa_pump_sc = spa_pump_sc

    def set_device_manager(self, dm) -> None:
        self._device_manager = dm

    # ────────────────────────── external LO helpers ──────────────────
    def _element_octave_rf_out(self, el: str) -> tuple[str, int] | None:
        """Return (octave_name, rf_out_port) for an element, or None."""
        cfg = self.qm_config or {}
        el_cfg = (cfg.get("elements") or {}).get(el)
        if not isinstance(el_cfg, dict):
            return None
        rf_in = el_cfg.get("RF_inputs")
        if not isinstance(rf_in, dict):
            return None
        port = rf_in.get("port")
        if isinstance(port, (list, tuple)) and len(port) >= 2:
            return (str(port[0]), int(port[1]))
        return None

    def _is_external_lo(self, el: str) -> bool:
        """True if the element's octave RF output uses an external LO source."""
        tup = self._element_octave_rf_out(el)
        if tup is None:
            return False
        octave_name, rf_port = tup
        cfg = self.qm_config or {}
        rf_outs = _get_nested(cfg, ["octaves", octave_name, "RF_outputs"], {})
        if not isinstance(rf_outs, dict):
            return False
        k = _key_like(rf_outs, rf_port)
        ch = rf_outs.get(k)
        if isinstance(ch, dict):
            return str(ch.get("LO_source", "")).lower() == "external"
        return False

    def _external_lo_info(self, el: str) -> dict | None:
        """
        Return external LO info dict for an element, or None.

        Reads __qubox.external_lo_map in hardware_extras, keyed by "octave:rf_out".

        Supports two formats:
        - Legacy string:  "oct1:2": "device_name"           → {"device": "device_name"}
        - Dict (preferred): "oct1:2": {"device": "...", "lo_port": "LO2"}
        """
        tup = self._element_octave_rf_out(el)
        if tup is None:
            return None
        octave_name, rf_port = tup
        qubox = (self.hardware_extras or {}).get("__qubox") or {}
        lo_map = qubox.get("external_lo_map") or {}
        entry = lo_map.get(f"{octave_name}:{rf_port}")
        if entry is None:
            return None
        if isinstance(entry, str):
            # Legacy format: just the device name
            return {"device": entry}
        if isinstance(entry, dict):
            return entry
        return None

    def _external_lo_device_name(self, el: str) -> str | None:
        """Return the DeviceManager device name for an element's external LO, or None."""
        info = self._external_lo_info(el)
        return info.get("device") if info else None

    @staticmethod
    def _parse_lo_source(s: str) -> OctaveLOSource | None:
        """Parse a string like 'LO2' into the corresponding OctaveLOSource enum value."""
        return _LO_SOURCE_MAP.get(s.lower().strip())

    def _configure_lo_source(self, el: str) -> None:
        """
        Tell the Octave which physical LO input port to route for an external-LO element.

        Per QM Octave docs: "LO source number must correspond to the up converter module number."
        E.g. RF output 2 must use OctaveLOSource.LO2.
        """
        info = self._external_lo_info(el)
        if info is None:
            return

        lo_port_str = info.get("lo_port")
        if lo_port_str is None:
            _logger.warning(
                f"No lo_port specified in external_lo_map for element '{el}'; "
                f"skipping set_lo_source. Add 'lo_port': 'LO2' (etc.) to the map entry."
            )
            return

        lo_port = self._parse_lo_source(lo_port_str)
        if lo_port is None:
            _logger.warning(
                f"Unknown lo_port '{lo_port_str}' for element '{el}'; "
                f"valid values: {list(_LO_SOURCE_MAP.keys())}. Skipping set_lo_source."
            )
            return

        self.qm.octave.set_lo_source(el, lo_port)
        _logger.info(f"set LO source for element '{el}' to {lo_port.name}")

    # ─────────────────────────── Hardware I/O ─────────────────────────
    def load_hardware(self, hardware_path: str | Path | None) -> None:
        hp = Path(hardware_path) if hardware_path else None
        if hp is not None:
            self.hardware_path = hp

        self.hardware_base = None
        self.hardware_extras = {}
        self._dirty["hardware"] = False

        if not self.hardware_path or not self.hardware_path.exists():
            _logger.info("No hardware file provided/found; continuing without hardware.")
            return

        raw = _numeric_keys_to_ints(json.loads(self.hardware_path.read_text()))
        self.hardware_base = _filter_hardware_schema(raw)
        self.hardware_extras = _select_keys(raw, self._hardware_extras_keys)
        _logger.info(f"Hardware loaded from {self.hardware_path}")

    def save_hardware(self, path: str | Path | None = None):
        _require(self.hardware_base is not None, "No hardware loaded to save.")
        dst = Path(path) if path else (self.hardware_path or Path("hardware.json"))
        to_write = {**deepcopy(self.hardware_base), **deepcopy(self.hardware_extras)}
        dst.write_text(_json_dump(to_write, indent=2))
        _logger.info(f"hardware saved → {dst}")
        self._dirty["hardware"] = False

    # ───────────────────────── Layered build/apply ─────────────────────
    def build_qm_config(self) -> dict:
        _require(self.hardware_base is not None, "No hardware loaded; cannot build QM config.")

        cfg = deepcopy(self.hardware_base)

        # Merge pulse overlay (top-level pulse blocks overwrite)
        if self.pulse_overlay:
            for k, v in self.pulse_overlay.items():
                cfg[k] = deepcopy(v)

        # Merge element operations overlay (deep merge so we don't clobber elements definitions)
        if self.element_ops_overlay:
            cfg = _deep_merge(cfg, self.element_ops_overlay)

        # Runtime overrides deep-merge (ephemeral patches)
        if self.runtime_overrides:
            cfg = _deep_merge(cfg, self.runtime_overrides)

        cfg = _numeric_keys_to_ints(cfg)
        cfg = _filter_qm_schema(cfg)

        if self._override_octave_json_mode is not None:
            _patch_octave_output_modes_in_json(cfg, self._override_octave_json_mode)

        return cfg


    def apply_changes(
        self,
        *,
        save_hardware: bool = False,
        allow_while_running: bool = False,
        stop_job: bool = True,
        init_elements: bool = True,
    ):
        with self._lock:
            if self.job and stop_job and not allow_while_running:
                with contextlib.suppress(Exception):
                    self.halt_job()

            new_cfg = self.build_qm_config()
            self.launch_qm(new_cfg)

            if init_elements:
                self.elements = self._init_elements()

            self.qm_config = new_cfg
            self._dirty = {"hardware": False, "pulses": False, "runtime": False}

            if save_hardware:
                self.save_hardware()

    def patch_hardware(
        self,
        patch_fn: Callable[[dict, dict], None],
        *,
        autosave: bool = False,
        autoburn: bool = True,
        allow_while_running: bool = False,
    ):
        _require(self.hardware_base is not None, "No hardware loaded; cannot patch.")
        _require(callable(patch_fn), "patch_fn must be callable")

        patch_fn(self.hardware_base, self.hardware_extras)
        self._dirty["hardware"] = True

        if autoburn:
            self.apply_changes(save_hardware=autosave, allow_while_running=allow_while_running)
        elif autosave:
            self.save_hardware()

    def patch_runtime(
        self,
        overrides: dict,
        *,
        autoburn: bool = True,
        allow_while_running: bool = False,
    ):
        _require(isinstance(overrides, dict), "overrides must be a dict")
        self.runtime_overrides = _deep_merge(self.runtime_overrides, overrides)
        self._dirty["runtime"] = True
        if autoburn:
            self.apply_changes(save_hardware=False, allow_while_running=allow_while_running)

    def clear_runtime_overrides(self, *, autoburn: bool = True):
        self.runtime_overrides = {}
        self._dirty["runtime"] = False
        if autoburn:
            self.apply_changes(save_hardware=False, allow_while_running=True)

    # ────────────────────── Pulses merger entry point ───────────────────
    def merge_pulses(self, pom: PulseOperationManager, *, include_volatile: bool = True, autoburn: bool = True):
        """
        Generate pulse overlay from POM without mutating persisted hardware.

        Also captures elements[*].operations injected by POM and stores it as a deep-merge overlay.
        """
        _require(self.hardware_base is not None, "Hardware is unknown; call load_hardware().")

        cfg = deepcopy(self.hardware_base)
        pom.burn_to_config(cfg, include_volatile=include_volatile)

        # store only generated top-level pulse blocks
        self.pulse_overlay = {k: deepcopy(cfg.get(k, {})) for k in _PULSE_KEYS}

        # store element operations as a *deep merge patch* so we don't clobber elements
        elems = cfg.get("elements") or {}
        ops_patch: dict = {}
        for el, el_cfg in elems.items():
            if not isinstance(el_cfg, dict):
                continue
            ops = el_cfg.get("operations")
            if isinstance(ops, dict) and ops:
                ops_patch.setdefault(el, {})["operations"] = deepcopy(ops)

        self.element_ops_overlay = {"elements": ops_patch} if ops_patch else {}

        self._dirty["pulses"] = True
        _logger.info("pulses merged into pulse_overlay (and element operations captured)")

        if autoburn:
            self.apply_changes(save_hardware=False, allow_while_running=True)


    def burn_pulse_to_qm(self, pom: PulseOperationManager | None = None, *, include_volatile: bool = True):
        if pom is not None:
            self.merge_pulses(pom, include_volatile=include_volatile, autoburn=True)
        else:
            self.apply_changes(save_hardware=False, allow_while_running=True)

    # ───────────────────────── existing API (updated) ────────────────
    def init_qm(self, config_dict: dict | None = None, *, init_elements: bool = True):
        if config_dict is not None:
            self.launch_qm(config_dict)
            if init_elements:
                self.elements = self._init_elements()
            return
        self.apply_changes(save_hardware=False, allow_while_running=True, init_elements=init_elements)

    def launch_qm(self, config_dict: dict, *, close_other_machines: bool = True):
        with self._lock:
            cfg_qm = _filter_qm_schema(_numeric_keys_to_ints(config_dict))
            try:
                self.qm = _with_retries(
                    lambda: self._qmm.open_qm(cfg_qm, close_other_machines=bool(close_other_machines))
                )
            except StreamTerminatedError as e:
                raise QMConnectionError("open_qm failed repeatedly.") from e

            self.qm_config = cfg_qm
            self.config_exists = True
            _logger.info("QM opened with provided configuration.")


    # ───────────────────────── Element alias helpers ───────────────────
    def _octave_links(self) -> list[dict]:
        ex = self.hardware_extras or {}
        if "octave_links" in ex:
            return list(ex["octave_links"] or [])
        qubox = ex.get("__qubox") or {}
        return list(qubox.get("octave_links") or [])

    def _build_ao_aliases_from_config(self) -> Dict[str, Dict[str, str]]:
        cfg = self.qm_config or {}
        elements = cfg.get("elements", {}) or {}
        links = self._octave_links()

        rf_to_ao: Dict[tuple[str, int], tuple[str, int, int]] = {}
        for it in links:
            try:
                rf_to_ao[(str(it["octave"]), int(it["rf_out"]))] = (
                    str(it["controller"]), int(it["ao_i"]), int(it["ao_q"])
                )
            except Exception:
                continue

        per_ctrl_alias: Dict[str, Dict[str, str]] = {}
        for el_name, el in elements.items():
            rf_in = el.get("RF_inputs")
            if not isinstance(rf_in, dict):
                continue
            port = rf_in.get("port")
            if not (isinstance(port, (list, tuple)) and len(port) >= 2):
                continue

            tup = rf_to_ao.get((str(port[0]), int(port[1])))
            if not tup:
                continue

            ctrl, ao_i, ao_q = tup
            m = per_ctrl_alias.setdefault(ctrl, {})
            m[f"1-{ao_i}"] = f"{el_name}:I"
            m[f"1-{ao_q}"] = f"{el_name}:Q"
        return per_ctrl_alias

    def _build_do_aliases_from_config(self) -> Dict[str, Dict[str, str]]:
        cfg = self.qm_config or {}
        elements = cfg.get("elements", {}) or {}
        per_ctrl_alias: Dict[str, Dict[str, str]] = {}
        for el_name, el in elements.items():
            for label, meta in (el.get("digitalInputs") or {}).items():
                port = meta.get("port")
                if not (isinstance(port, (list, tuple)) and len(port) >= 2):
                    continue
                ctrl, dport = str(port[0]), int(port[1])
                per_ctrl_alias.setdefault(ctrl, {})[f"1-{dport}"] = f"{el_name}:{label}"
        return per_ctrl_alias

    def _relabel_simulator_samples(self, sim) -> "SimulatorSamples":
        ao_aliases = self._build_ao_aliases_from_config()
        do_aliases = self._build_do_aliases_from_config()
        relabeled_by_ctrl = {}
        for ctrl, con in sim.items():
            a_alias = ao_aliases.get(ctrl, {})
            d_alias = do_aliases.get(ctrl, {})

            analog_new, sr_new = {}, {}
            for raw_key, arr in con.analog.items():
                new_key = a_alias.get(raw_key, raw_key)
                analog_new[new_key] = arr
                sr_new[new_key] = con.analog_sampling_rate.get(str(raw_key), 1e9)

            digital_new = {d_alias.get(k, k): v for k, v in con.digital.items()}

            relabeled_by_ctrl[ctrl] = SimulatorControllerSamples(
                analog=analog_new,
                digital=digital_new,
                analog_sampling_rate=sr_new,
            )
        return SimulatorSamples(relabeled_by_ctrl)


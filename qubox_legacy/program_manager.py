    @contextlib.contextmanager
    def _pump_on(self):
        try:
            if self.spa_pump_sc:
                self.spa_pump_sc.do_set_output_status(True)
            yield
        finally:
            if self.spa_pump_sc:
                with contextlib.suppress(Exception):
                    self.spa_pump_sc.do_set_output_status(False)

    def serialize_program(
        self,
        qua_prog=None,
        path: Path | str = "",
        filename: str = "debug.py",
        *,
        use_last_snapshot: bool = True,
    ) -> str:
        """
        Serialize a QUA program to a Python script using QM's generate_qua_script.

        If qua_prog is None, uses the most recently remembered program.
        If use_last_snapshot=True and we're serializing the most recent program,
        uses the config snapshot captured at submission/run time (more accurate
        than current qm_config if you later burned changes).
        """
        with self._lock:
            if qua_prog is None:
                qua_prog = self.current_program

            _require(qua_prog is not None, "No program has been run/queued/simulated yet; pass qua_prog explicitly.", QMJobError)

            # Prefer the snapshot if serializing the most recent program
            if use_last_snapshot and (qua_prog is self.current_program) and (self._last_program_cfg is not None):
                cfg = deepcopy(self._last_program_cfg)
            else:
                cfg = self.qm_config or self.build_qm_config()

        full_path = Path(path) / filename
        full_path.parent.mkdir(parents=True, exist_ok=True)

        with open(full_path, "w") as f:
            print(generate_qua_script(qua_prog, cfg), file=f)

        return str(full_path)

    def set_exec_mode(self, mode: ExecMode | str) -> None:
        self.exec_mode = _coerce_exec_mode(mode)
        _logger.info(f"QuaProgramManager exec_mode set to: {self.exec_mode}")

    def get_exec_mode(self) -> ExecMode:
        return self.exec_mode

    @staticmethod
    def _program_exec_mode(qua_prog) -> ExecMode | None:
        """
        Optional per-program declaration.

        Supported conventions:
        - qua_prog._exec_mode = ExecMode.SIMULATE / "simulate" / "hardware"
        - qua_prog._simulate_only = True  (shorthand for simulate)
        """
        if getattr(qua_prog, "_simulate_only", False):
            return ExecMode.SIMULATE

        m = getattr(qua_prog, "_exec_mode", None)
        if m is None:
            return None

        try:
            return _coerce_exec_mode(m)
        except Exception:
            # if user puts garbage there, fail loudly
            raise QMConfigError(f"Invalid qua_prog._exec_mode = {m!r}")

    def run_program(
        self,
        qua_prog,
        n_total: int,
        print_report: bool = True,
        show_progress: bool = True,
        processors: list | None = None,
        progress_handle: str = "iteration",
        auto_job_halt: bool = True,
        process_in_sim: bool | None = None,
        *,
        use_queue: bool = False,
        queue_to_start: bool = False,
        queue_only: bool = False,
        **kwargs,
    ) -> RunResult:
        """
        Hardware-only execution.

        Supports optional multi-user queue submission:
        - use_queue=True: submit to QM queue
        - queue_to_start=True: submit to front
        - queue_only=True: submit and return immediately (no wait / no fetch)

        Also records the most recent program + config snapshot for serialize_program().
        """
        # 1) manager-wide guard
        if self.exec_mode == ExecMode.SIMULATE:
            raise QMJobError(
                "run_program() is hardware-only, but QuaProgramManager is in ExecMode.SIMULATE. "
                "Call manager.set_exec_mode('hardware') or use manager.simulate(...)."
            )

        # 2) program-level guard
        p_mode = self._program_exec_mode(qua_prog)
        if p_mode == ExecMode.SIMULATE:
            raise QMJobError(
                "This QUA program is marked as SIMULATE-only. "
                "Use manager.simulate(qua_prog, ...) instead of run_program()."
            )

        self._require_qm()

        # Snapshot config NOW (so serialize_program matches what we submitted/ran,
        # even if you burn a new config later)
        cfg_snapshot = self.qm_config or self.build_qm_config()

        # Remember most recent program (works even for queue_only)
        if hasattr(self, "_remember_program"):
            self._remember_program(
                qua_prog,
                cfg=cfg_snapshot,
                meta={"last_mode": "hardware", "use_queue": bool(use_queue)},
            )
        else:
            # backward-compat if you didn't add _remember_program yet
            self.current_program = qua_prog

        out = Output()
        sim_samples = None  # always None in hardware path

        pending = None
        try:
            with self._pump_on():
                # ---- start job (queue or immediate) ----
                try:
                    if use_queue:
                        pending = (
                            self.qm.queue.add_to_start(qua_prog)
                            if queue_to_start
                            else self.qm.queue.add(qua_prog)
                        )

                        if queue_only:
                            meta = {
                                "n_total": int(n_total),
                                "queued": True,
                                "queue_only": True,
                                "pending_job": pending,
                                "job_id": getattr(pending, "job_id", None),
                                "time_added": getattr(pending, "time_added", None),
                                "user_added": getattr(pending, "user_added", None),
                            }
                            if hasattr(self, "_remember_program"):
                                self._remember_program(qua_prog, cfg=cfg_snapshot, meta=meta)

                            return RunResult(
                                mode=ExecMode.HARDWARE,
                                output=Output(),
                                sim_samples=None,
                                metadata=meta,
                            )

                        # Wait until it actually starts executing
                        self.job = pending.wait_for_execution()
                    else:
                        self.job = self.qm.execute(qua_prog)

                except StreamTerminatedError as e:
                    raise QMConnectionError("Connection lost during execute/queue.") from e
                except Exception as e:
                    raise QMJobError(f"Failed to start job: {e}") from e

                # ---- progress ----
                if show_progress:
                    self._report_progress(self.job, n_total, progress_handle)

                # ---- execution report ----
                if print_report:
                    with contextlib.suppress(Exception):
                        report = self.job.execution_report()
                        if report:
                            _logger.info("Execution report:\n%s", report)

                # ---- fetch results ----
                for name, handle in list(self.job.result_handles.items()):
                    try:
                        out[name] = handle.fetch_all()
                    except Exception as e:
                        _logger.error(f"Fetch failed for handle '{name}': {e}")

        finally:
            if auto_job_halt:
                with contextlib.suppress(Exception):
                    self.halt_job()

        # ---- processing ----
        procs = processors or self.processors

        if process_in_sim is None:
            process_in_sim = bool(_prog_flag(qua_prog, "_process_in_sim", False))

        # hardware-only: process if processors exist (ignore process_in_sim here)
        if procs:
            for proc in procs:
                try:
                    out = proc(out, **kwargs)
                except Exception as e:
                    pname = getattr(proc, "__name__", repr(proc))
                    _logger.error(f"Processor {pname} failed: {e}")

        meta = {"n_total": int(n_total)}
        if use_queue:
            meta.update(
                {
                    "queued": True,
                    "queue_only": False,
                    "job_id": getattr(pending, "job_id", None) if pending is not None else None,
                    "time_added": getattr(pending, "time_added", None) if pending is not None else None,
                    "user_added": getattr(pending, "user_added", None) if pending is not None else None,
                }
            )

        return RunResult(mode=ExecMode.HARDWARE, output=out, sim_samples=sim_samples, metadata=meta)



    def halt_job(self):
        if not self.job:
            return
        try:
            self.job.halt()
            _logger.info("Job Halted Successfully")
        except StreamTerminatedError:
            _logger.warning("Idle too long! Connection lost")
        except Exception as e:
            _logger.error(f"attempting to halt job failed: {e}")

    def _report_progress(self, job: RunningQmJob, n_total: int, handle: str):
        if not n_total:
            _logger.info("n_total not passed, will not report progress...")
            return

        handles = job.result_handles
        if handle not in handles.keys():
            _logger.warning("Progress handle not found; will not report progress.")
            while handles.is_processing():
                time.sleep(0.05)
            return

        show_bar = _logger.isEnabledFor(logging.INFO)
        with tqdm(total=n_total, desc="Running Program...", disable=not show_bar) as bar:
            while handles.is_processing():
                time.sleep(0.05)
                with contextlib.suppress(Exception):
                    h = handles.get(handle)
                    if h:
                        bar.n = h.fetch_all()
                        bar.refresh()

            with contextlib.suppress(Exception):
                h = handles.get(handle)
                if h:
                    bar.n = h.fetch_all()
                    bar.refresh()

